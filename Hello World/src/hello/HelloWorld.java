package hello;

public class HelloWorld {

	
	// All Java programs start in main method
	// The main method must be defined as:
	public static void main(String[] args) {
		// void - defines a function which has no return value
		// String[] args - is a parameter declaration
		// the parameter variable name is args
		// String[] is the type (array of strings)
		
		System.out.println("Hello World!");
		
	}
}
